/* -----------------------
  Helpers
----------------------- */
function uniq(list) {
  return Array.from(new Set((list || []).filter(Boolean)));
}

function buildAllList() {
  const keys = Object.keys(ASSET_MANIFEST);
  const all = [];
  keys.forEach((k) => {
    const arr = Array.isArray(ASSET_MANIFEST[k]) ? ASSET_MANIFEST[k] : [];
    arr.forEach((p) => all.push(p));
  });
  return uniq(all);
}

function toAbsoluteUrl(path) {
  try {
    return new URL(path, window.location.href).toString();
  } catch {
    return path;
  }
}

/* Remove leading "/" and keep clean zip paths */
function toZipPath(originalPath) {
  const p = String(originalPath || "").replace(/^\//, "").trim();
  // Strip only the top-level "assets/" so the zip starts clean.
  // Example: assets/brand-media-kit/... -> brand-media-kit/...
  return p.replace(/^assets\//, "");
}

async function fetchAsBlob(url) {
  const res = await fetch(toAbsoluteUrl(url), { cache: "no-cache" });
  if (!res.ok) throw new Error("Failed: " + url);
  return await res.blob();
}

async function downloadZipFromPaths(zipName, paths) {
  if (!window.JSZip) {
    alert("JSZip failed to load. Check your connection and try again.");
    return;
  }

  const zip = new JSZip();
  const list = uniq(paths);

  for (const path of list) {
    try {
      const blob = await fetchAsBlob(path);
      zip.file(toZipPath(path), blob);
    } catch (e) {
      console.warn(e);
    }
  }

  if (!list.length) {
    alert("No assets found to download. Update ASSET_MANIFEST in media-kit.js.");
    return;
  }

  const zipBlob = await zip.generateAsync({ type: "blob" });
  const url = URL.createObjectURL(zipBlob);

  const a = document.createElement("a");
  a.href = url;
  a.download = zipName;
  document.body.appendChild(a);
  a.click();
  a.remove();

  setTimeout(() => URL.revokeObjectURL(url), 1200);
}

async function handlePackDownload(packKey) {
  const key = String(packKey || "").trim();

  if (key === "all") {
    const allPaths = buildAllList();
    await downloadZipFromPaths("cavbot-brand-media-kit.zip", allPaths);
    return;
  }

  const list = ASSET_MANIFEST[key] || [];
  if (!Array.isArray(list) || !list.length) {
    alert("No files listed for: " + key + ". Update ASSET_MANIFEST in media-kit.js.");
    return;
  }

  let zipName = `cavbot-${key}.zip`;
  if (key === "brand-guideline-slides") zipName = "cavbot-brand-guidelines.zip";
  if (key === "product-screenshots") zipName = "cavbot-product-media.zip";

  await downloadZipFromPaths(zipName, list);
}

/* -----------------------
  Color Lab
  - Uses aria-pressed (no tab roles -> no tabpanel requirement)
----------------------- */
function hexToRgb(hex) {
  const clean = String(hex).replace("#", "").trim();
  if (clean.length !== 6) return null;
  const r = parseInt(clean.slice(0, 2), 16);
  const g = parseInt(clean.slice(2, 4), 16);
  const b = parseInt(clean.slice(4, 6), 16);
  if ([r, g, b].some((n) => Number.isNaN(n))) return null;
  return { r, g, b };
}

function rgbaString(rgb, alpha) {
  return `rgba(${rgb.r}, ${rgb.g}, ${rgb.b}, ${alpha})`;
}

function initColorLab() {
  const lab = document.querySelector("[data-color-lab]");
  if (!lab) return;

  const chips = Array.from(lab.querySelectorAll(".color-chip"));
  const slider = lab.querySelector(".alpha-range");
  const alphaValueEl = lab.querySelector("[data-alpha-value]");

  const titleEl = lab.querySelector("[data-color-title]");
  const roleEl = lab.querySelector("[data-color-role]");
  const hexEl = lab.querySelector("[data-color-hex]");
  const rgbaEl = lab.querySelector("[data-color-rgba]");

  const previews = {
    fill: lab.querySelector('[data-preview="fill"]'),
    stroke: lab.querySelector('[data-preview="stroke"]'),
    glass: lab.querySelector('[data-preview="glass"]'),
    text: lab.querySelector('[data-preview="text"]')
  };

  let activeHex = "#8B5CFF";
  let activeName = "Hyper Violet";
  let activeRole = "Primary signal";

  function setActiveChip(btn) {
    chips.forEach((c) => {
      const isActive = c === btn;
      c.classList.toggle("is-active", isActive);
      c.setAttribute("aria-pressed", isActive ? "true" : "false");
    });

    activeHex = btn.getAttribute("data-color-hex") || activeHex;
    activeName = btn.getAttribute("data-color-name") || activeName;
    activeRole = btn.getAttribute("data-color-role") || activeRole;

    if (titleEl) titleEl.textContent = activeName;
    if (roleEl) roleEl.textContent = activeRole;
    if (hexEl) hexEl.textContent = activeHex.toUpperCase();

    updatePreview();
  }

  function updatePreview() {
    const alphaPercent = Number(slider?.value || 100);
    const alpha = Math.max(0.10, Math.min(1, alphaPercent / 100));
    if (alphaValueEl) alphaValueEl.textContent = String(alphaPercent);

    const rgb = hexToRgb(activeHex);
    if (!rgb) return;

    const rgba = rgbaString(rgb, alpha);
    if (rgbaEl) rgbaEl.textContent = rgba;

    if (previews.fill) {
      previews.fill.style.background = rgba;
      previews.fill.style.borderColor = "rgba(255,255,255,0.10)";
    }

    if (previews.stroke) {
      previews.stroke.style.background = "transparent";
      previews.stroke.style.border = `2px solid ${rgba}`;
    }

    if (previews.glass) {
      previews.glass.style.background = `linear-gradient(180deg, ${rgba}, rgba(2,6,22,0.12))`;
      previews.glass.style.borderColor = "rgba(255,255,255,0.10)";

      const chip = previews.glass.querySelector(".glass-chip");
      if (chip) chip.style.background = rgba;

      const lines = previews.glass.querySelectorAll(".glass-lines span");
      lines.forEach((line, i) => {
        line.style.background =
          i === 0
            ? rgba
            : `rgba(${rgb.r}, ${rgb.g}, ${rgb.b}, ${Math.max(0.08, alpha * 0.45)})`;
      });
    }

    if (previews.text) {
      previews.text.style.background = "rgba(255,255,255,0.04)";
      const p = previews.text.querySelector(".text-demo");
      if (p) p.style.color = rgba;
    }
  }

  chips.forEach((btn) => {
    btn.addEventListener("click", () => setActiveChip(btn));
  });

  slider?.addEventListener("input", updatePreview);

  const first = chips.find((c) => c.classList.contains("is-active")) || chips[0];
  if (first) setActiveChip(first);
  updatePreview();
}

/* -----------------------
  Copy to clipboard
----------------------- */
function initCopyButtons() {
  function markCopied(btn) {
    btn.classList.add("is-copied");
    const prevLabel = btn.getAttribute("aria-label") || "Copy";
    btn.setAttribute("aria-label", "Copied");
    clearTimeout(btn._copyResetTimer);
    btn._copyResetTimer = setTimeout(() => {
      btn.classList.remove("is-copied");
      btn.setAttribute("aria-label", prevLabel);
    }, 1200);
  }

  document.addEventListener("click", async (e) => {
    const btn = e.target.closest("[data-copy]");
    if (!btn) return;

    const sel = btn.getAttribute("data-copy");
    const target = sel ? document.querySelector(sel) : null;
    if (!target) return;

    const text = target.textContent ? target.textContent.trim() : "";
    if (!text) return;

    try {
      await navigator.clipboard.writeText(text);
      markCopied(btn);
    } catch {
      const ta = document.createElement("textarea");
      ta.value = text;
      ta.setAttribute("readonly", "");
      ta.style.position = "absolute";
      ta.style.left = "-9999px";
      document.body.appendChild(ta);
      ta.select();
      document.execCommand("copy");
      ta.remove();
      markCopied(btn);
    }
  });
}

/* -----------------------
  Downloads + init
----------------------- */
document.addEventListener("click", (e) => {
  const btn = e.target.closest("[data-download-pack]");
  if (!btn) return;

  e.preventDefault();
  const packKey = btn.getAttribute("data-download-pack");
  handlePackDownload(packKey);
});

document.addEventListener("DOMContentLoaded", () => {
  initColorLab();
  initCopyButtons();
});
